export function localiseMythTitle(title: string, locale: Locale) {
  if (locale === 'en') return title
  if (sceneTitleTr[title]) return sceneTitleTr[title]
  return title
    .replace(/\band\b/g, 've')
    .replace(/^The Birth of (.+)$/i, '$1’in Doğuşu')
    .replace(/^The Return of (.+)$/i, '$1’in Dönüşü')
}
